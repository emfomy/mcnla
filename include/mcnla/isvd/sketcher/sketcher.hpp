#ifndef MCNLA_ISVD_SKETCHER_SKETCHER_HPP_
#define MCNLA_ISVD_SKETCHER_SKETCHER_HPP_

#include <mcnla/isvd/def.hpp>
#include <mcnla/isvd/sketcher/sketcher_wrapper.hpp>
#include <mcnla/core/utility/traits.hpp>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  The MCNLA namespace.
//
namespace mcnla {

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  The iSVD namespace.
//
namespace isvd {

#ifndef DOXYGEN_SHOULD_SKIP_THIS
template <class _Tag, typename _Val> class Sketcher;
#endif  // DOXYGEN_SHOULD_SKIP_THIS

}  // namespace isvd

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  The traits namespace.
//
namespace traits {

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// The sketcher traits.
///
template <class _Tag, typename _Val>
struct Traits<isvd::Sketcher<_Tag, _Val>> {
  using ValType = _Val;
};

}  // namespace traits

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  The iSVD namespace.
//
namespace isvd {

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @ingroup  isvd_sketcher_module_detail
/// The interface of iSVD sketcher.
///
/// @tparam  _Tag  The sketcher tag.
/// @tparam  _Val  The value type.
///
template <class _Tag, typename _Val>
class Sketcher : public SketcherWrapper<Sketcher<_Tag, _Val>> {
  static_assert(traits::False<_Tag>::value, "Error using non-specialized iSVD sketcher!");
};

}  // namespace isvd

}  // namespace mcnla

#endif  // MCNLA_ISVD_SKETCHER_SKETCHER_HPP_
