#include <cmath>
#include <complex>
#include <mcnla/core/matrix.hpp>
#include <mcnla/core/blas/blas/nrm2.hpp>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  The MCNLA namespace
//
namespace mcnla {

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  The BLAS namespace
//
namespace blas {

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @ingroup  blas1_module
/// @brief  Computes the Euclidean norm of a vector.
///
//@{
template <typename _Scalar>
inline RealType<_Scalar> nrm2(
    const DenseVector<_Scalar> &x
) noexcept {
  return detail::nrm2(x.getLength(), x.getValuePtr(), x.getStride());
}
//@}

}  // namespace blas

}  // namespace mcnla

#endif  // MCNLA_CORE_BLAS_ROUTINE_NRM2_HPP_
