namespace mcnla {

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  The BLAS namespace
//
namespace blas {

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @ingroup  blas1_module
/// @brief  Computes a vector-vector dot product.
///
template <typename _Scalar>
inline _Scalar dot(
    const DenseVector<_Scalar> &x,
    const DenseVector<_Scalar> &y
) noexcept {
  mcnla_assert_eq(x.getSizes(), y.getSizes());
  return detail::dot(x.getLength(), x.getValuePtr(), x.getStride(), y.getValuePtr(), y.getStride());
}

}  // namespace blas

}  // namespace mcnla

#endif  // MCNLA_CORE_BLAS_ROUTINE_DOT_HPP_
