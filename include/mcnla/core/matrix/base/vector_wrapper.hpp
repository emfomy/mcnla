#ifndef MCNLA_CORE_MATRIX_BASE_VECTOR_WRAPPER_HPP_
#define MCNLA_CORE_MATRIX_BASE_VECTOR_WRAPPER_HPP_

#include <mcnla/core/matrix/base/vector_wrapper.hh>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  The MCNLA namespace.
//
namespace mcnla {

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  The matrix namespace.
//
namespace matrix {

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief  Gets the length.
///
template <class _Derived>
index_t VectorWrapper<_Derived>::len() const noexcept {
  return this->derived().lenImpl();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief  Gets the number of elements.
///
template <class _Derived>
index_t VectorWrapper<_Derived>::nelem() const noexcept {
  return len();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief  Gets the size of dimensions in interface order: `{ #len }`.
///
template <class _Derived>
std::tuple<index_t> VectorWrapper<_Derived>::sizes() const noexcept {
  return std::make_tuple(len());
}

}  // namespace matrix

}  // namespace mcnla

#endif  // MCNLA_CORE_MATRIX_BASE_VECTOR_WRAPPER_HPP_
